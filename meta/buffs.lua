-- Get the id of all buffs that share this name
function meta:get_ids_by_name(name)
    if type(name) == 'string' then
        name = string.lower(name)
        return self.buffs_by_name[name]
    end
end

-------------------------------------------------------------------------------
-- Get the ids of all buffs that share a name with the provided buff id
function meta:get_with_shared_name_by_id(id)
    if type(id) == 'number' then
        return self.buffs_by_id[id]
    end
end

-------------------------------------------------------------------------------
-- Given a buff name and an array of buffs (such as that obtained via 
-- windower.ffxi.get_player().buffs), determine if the named buff is present.
function meta:has_buff(name, buffs)
    if type(buffs) == 'table' and type(name) == 'string' then
        name = string.lower(name)
        local ids = self.buffs_by_name[name]
        if ids then
            for key, buff_id in pairs(buffs) do
                if ids[buff_id] then
                    return buff_id
                end
            end
        end
    end
end

